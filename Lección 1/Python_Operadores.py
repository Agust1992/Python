
#Operadores
vOperandoA = 3
vOperandoB = 2
vSuma = vOperandoB + vOperandoA
print(f'Resultado Suma: {vSuma} ')

###################################################
vResta = (vOperandoA - vOperandoB)
print(f'Resultado Resta: {vResta} ')
###################################################
vProducto = (vOperandoA * vOperandoB)
print(f'Resultado Multiplicación: {vProducto}')
###################################################
vDivision = (vOperandoB/vOperandoA)
print(f'Resultado División: {vDivision}')
###################################################
vModulo = (vOperandoB % vOperandoA)
print(f'Resultado Módulo: {vModulo}')
###################################################
vExponente = (vOperandoA**vOperandoB)
print(f'Resultado Exponente: {vExponente}')